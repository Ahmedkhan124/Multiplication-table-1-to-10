// 5 times table

// Initialize the multiplier variable starts at 1
let multiplier: number = 1;

// Define the constant for the 5 times table.
const timesTable: number = 5

// using a while loop to generate the 5 times table.
while (multiplier <= 10){
    console.log(`${timesTable} x ${multiplier} = ${timesTable * multiplier}`);

    // increment the multiplier.
    multiplier++;   
}