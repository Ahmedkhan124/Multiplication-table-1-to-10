//6 times table
// Initialize the counter variable to 1
let table = 6;
// define the multiplier for the times table
let multiply = 1;
// use a do...while loop to generate the six times table
do {
    console.log(`${table} x ${multiply} = ${table * multiply}`);
    // increment the variable.
    multiply++;
} while (multiply <= 10); // continue the loop until multiply exceed 10
